package sitv.s003.dianbo.com.appmarkettwo.bean;

public class SearchTag {
	private int id;
	private int count;
	private String pinyin;
	private String name;
	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}



	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}



	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}



	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}






}
